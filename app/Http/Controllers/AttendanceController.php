     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        for($i=0;$i<count($request->present);$i++){
            $attendance = new Attendance;
            $attendance->employee_id = $request->employee_id[$i];
            $attendance->present = $request->present[$i];
            $attendance->absent = $request->absent[$i];
            $attendance->leave = $request->leave[$i];
            $attendance->mounth = $request->month;
            $attendance->year = $request->year;
            $attendance->save();
        }
        return response()->json(['success'=>'Data Add successfully.']);
    }

